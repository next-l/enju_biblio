class CarrierType < ActiveRecord::Base
  include MasterModel
  default_scope { order("carrier_types.position") }
  has_many :manifestations

  def mods_type
    case name
    when 'volume'
      'text'
    else
      # TODO: その他のタイプ
      'software, multimedia'
    end
  end
end

