class CarrierType < ApplicationRecord
  include MasterModel
  translates :display_name
  has_many :manifestations

  has_one_attached :attachment

  def mods_type
    case name
    when 'volume'
      'text'
    else
      # TODO: その他のタイプ
      'software, multimedia'
    end
  end
end

